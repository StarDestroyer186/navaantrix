<script>
	document.addEventListener("DOMContentLoaded", function () {
		document.getElementById("contact-form").addEventListener("submit", function (e) {
			e.preventDefault(); // Prevent default form submission

			var xhr = new XMLHttpRequest();
			xhr.open("POST", "<?php echo base_url('submit_contact'); ?>", true);
			xhr.setRequestHeader("Content-Type", "application/x-www-form-urlencoded");

			xhr.onreadystatechange = function () {
				if (xhr.readyState === 4) {
					if (xhr.status === 200) {
						var response = JSON.parse(xhr.responseText);
						if (response.status == "success") {
							document.getElementById("ajaxsuccess").style.display = "block";
							document.getElementById("ajaxsuccess").textContent = response.message;
							document.getElementById("contact-form").reset();
						} else {
							document.getElementById("err-form").style.display = "block";
							document.getElementById("err-form").textContent = response.message;
						}
					} else {
						document.getElementById("err-timedout").style.display = "block";
					}
				}
			};

			var formData = new FormData(document.getElementById("contact-form"));
			var encodedData = new URLSearchParams(formData).toString();
			xhr.send(encodedData);
		});
	});
</script>
